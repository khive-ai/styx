//! Direct Rust→Lean emitter (no LLBC/Charon).
//!
//! Reads parsed Rust type definitions and function signatures and emits
//! standalone Lean 4 files:
//!   - Types.lean           — structure, inductive, abbrev declarations
//!   - Funs.lean            — opaque function declarations
//!   - Bridge_Template.lean — correspondence proof outlines (--bridge-template)
//!
//! This is the `--direct` mode emission backend.

use std::collections::{BTreeMap, HashMap};
use std::path::{Path, PathBuf};

use crate::emit::indent::IndentWriter;
use crate::naming::{escape_lean_ident, escape_lean_keyword};
use crate::rust_parser::{
    ParsedCrate, ParsedField, ParsedFnSig, ParsedTypeDef, ParsedTypeKind, ParsedVariant, RustType,
    rust_type_to_lean,
};
use crate::type_rewrite::RewriteRules;

/// Emit Lean 4 files from directly-parsed Rust source.
///
/// Returns a map of output file paths to their contents.
pub fn emit_direct(
    parsed: &ParsedCrate,
    namespace: &str,
    output_dir: &PathBuf,
    emit_checks: bool,
) -> BTreeMap<PathBuf, String> {
    let rules = RewriteRules::standard();
    let mut files = BTreeMap::new();

    let ns_dir = output_dir.join(namespace);

    // Types.lean
    let types_content = emit_types_file(parsed, namespace, &rules, emit_checks);
    files.insert(ns_dir.join("Types.lean"), types_content);

    // Funs.lean (only if there are functions)
    if !parsed.fns.is_empty() {
        let funs_content = emit_funs_file(parsed, namespace, &rules, emit_checks);
        files.insert(ns_dir.join("Funs.lean"), funs_content);
    }

    files
}

// ---------------------------------------------------------------------------
// Types.lean
// ---------------------------------------------------------------------------

fn emit_types_file(
    parsed: &ParsedCrate,
    namespace: &str,
    rules: &RewriteRules,
    emit_checks: bool,
) -> String {
    let mut w = IndentWriter::new();

    // Header
    w.writeln("-- This file was automatically generated by styx (direct mode).");
    w.writeln("-- Do not edit manually.");
    w.newline();
    w.writeln(format!("namespace {namespace}"));
    w.newline();

    // Filter out std/core/alloc types
    let types: Vec<&ParsedTypeDef> = parsed
        .types
        .iter()
        .filter(|t| {
            !t.name.starts_with("std.")
                && !t.name.starts_with("core.")
                && !t.name.starts_with("alloc.")
        })
        .collect();

    if types.is_empty() {
        w.writeln("-- No public types found.");
    }

    for def in &types {
        emit_type_def(&mut w, def, rules, emit_checks);
    }

    w.writeln(format!("end {namespace}"));
    w.finish()
}

/// Emit a single Lean type declaration from a parsed Rust type definition.
fn emit_type_def(
    w: &mut IndentWriter,
    def: &ParsedTypeDef,
    rules: &RewriteRules,
    emit_checks: bool,
) {
    let name = &def.simple_name;

    // Source comment.
    w.writeln(format!("-- Source: {}", def.source_file));

    // Generic params.
    let gp = format_generic_params(&def.type_params);

    match &def.kind {
        ParsedTypeKind::Struct(fields) => {
            emit_struct(w, name, &gp, fields, rules);
        }
        ParsedTypeKind::Enum(variants) => {
            emit_enum(w, name, &gp, variants, rules);
        }
        ParsedTypeKind::Alias(target) => {
            let lean_ty = rust_type_to_lean(target, rules);
            w.writeln(format!("abbrev {name}{gp} := {lean_ty}"));
            w.newline();
        }
    }

    if emit_checks {
        w.writeln(format!("#check @{name}"));
        w.newline();
    }
}

fn emit_struct(
    w: &mut IndentWriter,
    name: &str,
    gp: &str,
    fields: &[ParsedField],
    rules: &RewriteRules,
) {
    if fields.is_empty() {
        // Unit struct.
        w.writeln(format!("structure {name}{gp} where"));
        w.newline();
        return;
    }

    w.writeln(format!("structure {name}{gp} where"));
    w.indent();
    for f in fields {
        let lean_ty = rust_type_to_lean(&f.ty, rules);
        let fname = escape_lean_keyword(&f.name);
        w.writeln(format!("{fname} : {lean_ty}"));
    }
    w.dedent();
    w.newline();
}

fn emit_enum(
    w: &mut IndentWriter,
    name: &str,
    gp: &str,
    variants: &[crate::rust_parser::ParsedVariant],
    rules: &RewriteRules,
) {
    w.writeln(format!("inductive {name}{gp} where"));
    w.indent();
    for v in variants {
        let vname = escape_lean_keyword(&v.name);
        if v.fields.is_empty() {
            w.writeln(format!("| {vname}"));
        } else {
            let args: Vec<String> = v
                .fields
                .iter()
                .map(|f| {
                    let lean_ty = rust_type_to_lean(&f.ty, rules);
                    format!("({} : {})", f.name, lean_ty)
                })
                .collect();
            w.writeln(format!("| {vname} {}", args.join(" ")));
        }
    }
    w.dedent();
    w.newline();
}

