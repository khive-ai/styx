fn emit_funs_file(
    parsed: &ParsedCrate,
    namespace: &str,
    rules: &RewriteRules,
    emit_checks: bool,
) -> String {
    let mut w = IndentWriter::new();

    // Header
    w.writeln("-- This file was automatically generated by styx (direct mode).");
    w.writeln("-- Do not edit manually.");
    w.newline();
    w.writeln(format!("import {namespace}.Types"));
    w.newline();
    w.writeln(format!("namespace {namespace}"));
    w.newline();

    // Group functions by self_type for readability.
    let mut by_type: BTreeMap<Option<String>, Vec<&ParsedFnSig>> = BTreeMap::new();
    for f in &parsed.fns {
        by_type.entry(f.self_type.clone()).or_default().push(f);
    }

    // Emit free functions first.
    if let Some(free_fns) = by_type.remove(&None) {
        w.writeln("-- Free functions");
        w.newline();
        for f in free_fns {
            emit_fn_decl(&mut w, f, rules, emit_checks);
        }
    }

    // Emit method groups.
    for (type_name, methods) in &by_type {
        if let Some(tn) = type_name {
            w.writeln(format!("-- Methods: {tn}"));
            w.newline();
            for m in methods {
                emit_fn_decl(&mut w, m, rules, emit_checks);
            }
        }
    }

    w.writeln(format!("end {namespace}"));
    w.finish()
}

/// Emit a single opaque function declaration.
fn emit_fn_decl(w: &mut IndentWriter, f: &ParsedFnSig, rules: &RewriteRules, emit_checks: bool) {
    let name = &f.name;

    // Source comment.
    w.writeln(format!("-- Source: {}", f.source_file));

    // Generic params.
    let gp = format_generic_params(&f.type_params);

    // Build parameter list (resolve Self to concrete type).
    let params_str = format_params(&f.params, rules, f.self_type.as_deref());

    // Build return type.
    let ret_str = format_return_type(f, rules);

    w.writeln(format!("opaque {name}{gp}{params_str} : {ret_str}"));
    w.newline();

    if emit_checks {
        w.writeln(format!("#check @{name}"));
        w.newline();
    }
}

