/// Correspondence proof strength for a directly-parsed type.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum DirectBridgeKind {
    /// Enum with unit-only constructors → full Equiv (roundtrip proofs).
    Bijective,
    /// Enum with data-carrying constructors → one-way injective.
    Injective,
    /// Struct → relational correspondence (field-by-field Prop structure).
    Relational,
}

/// Classify a `ParsedTypeDef` for bridge correspondence scaffolding.
///
/// Returns `None` for type aliases and unit structs (not interesting for bridges).
fn classify_direct(def: &ParsedTypeDef) -> Option<DirectBridgeKind> {
    match &def.kind {
        ParsedTypeKind::Enum(variants) => {
            if variants.iter().all(|v| v.fields.is_empty()) {
                Some(DirectBridgeKind::Bijective)
            } else {
                Some(DirectBridgeKind::Injective)
            }
        }
        ParsedTypeKind::Struct(fields) => {
            if fields.is_empty() {
                None // Unit struct — skip
            } else {
                Some(DirectBridgeKind::Relational)
            }
        }
        ParsedTypeKind::Alias(_) => None,
    }
}

/// Registry mapping simple type names to their bridge classification.
///
/// Used by `direct_field_correspondence` to emit concrete patterns for
/// nested ADT fields rather than sorry placeholders.
type DirectRegistry = HashMap<String, DirectBridgeKind>;

/// Derive a camelCase function prefix from a simple type name.
/// E.g., `SecurityLevel` → `securityLevel`.
fn derive_prefix(simple_name: &str) -> String {
    if simple_name.is_empty() {
        return "unknown".to_string();
    }
    let mut chars = simple_name.chars();
    match chars.next() {
        Some(c) => format!("{}{}", c.to_lowercase(), chars.as_str()),
        None => "unknown".to_string(),
    }
}

/// Derive a spec type name for a simple extracted type name.
///
/// If `spec_map` contains an entry for `simple_name`, that path is used.
/// Otherwise falls back to `Lion.{simple_name}` with a TODO comment.
fn derive_direct_spec_name(
    simple_name: &str,
    spec_map: Option<&HashMap<String, String>>,
) -> (String, bool) {
    if let Some(map) = spec_map
        && let Some(path) = map.get(simple_name) {
            return (path.clone(), true);
        }
    (format!("Lion.{simple_name}"), false)
}

/// Return true if a `RustType` is a bounded integer scalar (u8…u64, usize, i8…i64, isize).
fn rust_type_is_bounded_scalar(ty: &RustType) -> bool {
    match ty {
        RustType::Named(name) => matches!(
            name.as_str(),
            "u8" | "u16"
                | "u32"
                | "u64"
                | "u128"
                | "usize"
                | "i8"
                | "i16"
                | "i32"
                | "i64"
                | "i128"
                | "isize"
        ),
        _ => false,
    }
}

/// Return true if a `RustType` is `bool`.
fn rust_type_is_bool(ty: &RustType) -> bool {
    matches!(ty, RustType::Named(n) if n == "bool")
}

/// Generate the field equality expression for a struct field in direct mode.
///
/// The `registry` maps simple type names to their `DirectBridgeKind`.
fn direct_field_correspondence(
    field_name: &str,
    ty: &RustType,
    registry: &DirectRegistry,
) -> String {
    let ef = format!("ec.{field_name}");
    let sf = format!("sc.{field_name}");

    // Strip references/pointers transparently.
    let ty = match ty {
        RustType::Ref(inner) | RustType::RawPtr(inner) => inner.as_ref(),
        other => other,
    };

    if rust_type_is_bounded_scalar(ty) {
        // U64 → Nat: extract .val
        format!("{ef}.val = {sf}")
    } else if rust_type_is_bool(ty) {
        format!("{ef} = {sf}")
    } else if matches!(ty, RustType::Named(n) if n == "String" || n == "str") {
        format!("sorry /- TODO: String correspondence for {field_name} -/")
    } else {
        match ty {
            RustType::Generic(base, args) if base == "Option" || base == "core::option::Option" => {
                if let Some(inner_ty) = args.first() {
                    if rust_type_is_bounded_scalar(inner_ty) {
                        return format!("{ef}.map (·.val) = {sf}");
                    }
                    if let RustType::Named(inner_name) = inner_ty
                        && let Some(kind) = registry.get(inner_name.as_str()) {
                            let prefix = derive_prefix(inner_name);
                            match kind {
                                DirectBridgeKind::Bijective | DirectBridgeKind::Injective => {
                                    return format!("{ef}.map {prefix}ToSpec = {sf}");
                                }
                                DirectBridgeKind::Relational => {
                                    return format!(
                                        "sorry /- TODO: Option<{inner_name}> correspondence for {field_name} -/"
                                    );
                                }
                            }
                        }
                }
                format!("{ef} = {sf} -- TODO: verify Option mapping")
            }
            RustType::Generic(base, args)
                if base == "Vec" || base == "alloc::vec::Vec" || base == "std::vec::Vec" =>
            {
                if let Some(inner_ty) = args.first() {
                    if rust_type_is_bounded_scalar(inner_ty) {
                        // Vec<u64> etc. → extract .val on each element
                        return format!("{ef}.map (·.val) = {sf}");
                    }
                    if let RustType::Named(inner_name) = inner_ty
                        && let Some(kind) = registry.get(inner_name.as_str()) {
                            let prefix = derive_prefix(inner_name);
                            match kind {
                                DirectBridgeKind::Bijective | DirectBridgeKind::Injective => {
                                    return format!("{ef}.map {prefix}ToSpec = {sf}");
                                }
                                DirectBridgeKind::Relational => {
                                    let corr = format!("{inner_name}Corresponds");
                                    return format!("List.Forall₂ {corr} {ef} {sf}");
                                }
                            }
                        }
                }
                format!("sorry /- TODO: Vec correspondence for {field_name} -/")
            }
            RustType::Named(type_name) => {
                if let Some(kind) = registry.get(type_name.as_str()) {
                    let prefix = derive_prefix(type_name);
                    match kind {
                        DirectBridgeKind::Bijective | DirectBridgeKind::Injective => {
                            format!("{prefix}ToSpec {ef} = {sf}")
                        }
                        DirectBridgeKind::Relational => {
                            format!("{type_name}Corresponds {ef} {sf}")
                        }
                    }
                } else {
                    // Unknown named type — keep sorry
                    format!("sorry /- TODO: {type_name} correspondence for {field_name} -/")
                }
            }
            _ => format!("{ef} = {sf}"),
        }
    }
}

/// Emit BIJECTIVE correspondence for a unit-only enum.
fn emit_direct_bijective(
    w: &mut IndentWriter,
    name: &str,
    namespace: &str,
    spec: &str,
    spec_known: bool,
    variants: &[ParsedVariant],
) {
    let extracted = format!("{namespace}.{name}");
    let prefix = derive_prefix(name);
    let spec_comment = if spec_known {
        spec.to_string()
    } else {
        format!("{spec}  -- TODO: verify spec type path")
    };

    w.writeln("-- ───────────────────────────────────────────────────────────────");
    w.writeln(format!("-- {name} (BIJECTIVE — enum, unit constructors)"));
    w.writeln(format!("-- Extracted: {extracted}"));
    w.writeln(format!("-- Spec:      {spec_comment}"));
    w.writeln("-- ───────────────────────────────────────────────────────────────");
    w.newline();

    // toSpec
    w.writeln(format!("def {prefix}ToSpec : {extracted} → {spec}"));
    for v in variants {
        let vname = escape_lean_ident(&v.name);
        w.writeln(format!(
            "  | .{vname} => .{vname}  -- TODO: verify constructor name"
        ));
    }
    w.newline();

    // fromSpec
    w.writeln(format!("def {prefix}FromSpec : {spec} → {extracted}"));
    for v in variants {
        let vname = escape_lean_ident(&v.name);
        w.writeln(format!(
            "  | .{vname} => .{vname}  -- TODO: verify constructor name"
        ));
    }
    w.newline();

    // Roundtrip theorems
    w.writeln(format!("theorem {prefix}_roundtrip (s : {spec}) :"));
    w.writeln(format!("    {prefix}ToSpec ({prefix}FromSpec s) = s := by"));
    w.writeln("  cases s <;> rfl");
    w.newline();

    w.writeln(format!(
        "theorem {prefix}_roundtrip_inv (e : {extracted}) :"
    ));
    w.writeln(format!("    {prefix}FromSpec ({prefix}ToSpec e) = e := by"));
    w.writeln("  cases e <;> rfl");
    w.newline();

    // Equiv bundle
    w.writeln(format!("def {prefix}Equiv : {extracted} ≃ {spec} where"));
    w.writeln(format!("  toFun := {prefix}ToSpec"));
    w.writeln(format!("  invFun := {prefix}FromSpec"));
    w.writeln(format!("  left_inv := {prefix}_roundtrip_inv"));
    w.writeln(format!("  right_inv := {prefix}_roundtrip"));
    w.newline();
}

/// Emit INJECTIVE correspondence for an enum with data constructors.
fn emit_direct_injective(
    w: &mut IndentWriter,
    name: &str,
    namespace: &str,
    spec: &str,
    spec_known: bool,
    variants: &[ParsedVariant],
) {
    let extracted = format!("{namespace}.{name}");
    let prefix = derive_prefix(name);
    let spec_comment = if spec_known {
        spec.to_string()
    } else {
        format!("{spec}  -- TODO: verify spec type path")
    };

    w.writeln("-- ───────────────────────────────────────────────────────────────");
    w.writeln(format!(
        "-- {name} (INJECTIVE — enum with data constructors)"
    ));
    w.writeln(format!("-- Extracted: {extracted}"));
    w.writeln(format!("-- Spec:      {spec_comment}"));
    w.writeln("-- ───────────────────────────────────────────────────────────────");
    w.newline();

    // toSpec
    w.writeln(format!("def {prefix}ToSpec : {extracted} → {spec}"));
    for v in variants {
        let vname = escape_lean_ident(&v.name);
        if v.fields.is_empty() {
            w.writeln(format!("  | .{vname} => .{vname}  -- TODO: verify"));
        } else {
            let args: Vec<String> = (0..v.fields.len()).map(|i| format!("a{i}")).collect();
            let arg_str = args.join(" ");
            let mappings: Vec<String> = v
                .fields
                .iter()
                .enumerate()
                .map(|(i, f)| {
                    if rust_type_is_bounded_scalar(&f.ty) {
                        format!("a{i}.val")
                    } else {
                        format!("a{i}")
                    }
                })
                .collect();
            w.writeln(format!(
                "  | .{vname} {arg_str} => .{vname} {}  -- TODO: verify field mappings",
                mappings.join(" ")
            ));
        }
    }
    w.newline();

    // Injectivity theorem
    w.writeln(format!(
        "theorem {prefix}ToSpec_injective : Function.Injective {prefix}ToSpec := by"
    ));
    w.writeln("  intro x y h");
    w.writeln(format!(
        "  cases x <;> cases y <;> simp_all [{prefix}ToSpec]"
    ));
    w.writeln("  -- TODO: fill in remaining cases (use UScalar.eq_of_val_eq for bounded fields)");
    w.newline();
}

/// Emit RELATIONAL correspondence for a struct.
fn emit_direct_relational(
    w: &mut IndentWriter,
    name: &str,
    namespace: &str,
    spec: &str,
    spec_known: bool,
    fields: &[ParsedField],
    registry: &DirectRegistry,
) {
    let extracted = format!("{namespace}.{name}");
    let has_bounded = fields.iter().any(|f| rust_type_is_bounded_scalar(&f.ty));
    let strength_note = if has_bounded {
        "RELATIONAL — struct with bounded fields"
    } else {
        "RELATIONAL — struct"
    };
    let spec_comment = if spec_known {
        spec.to_string()
    } else {
        format!("{spec}  -- TODO: verify spec type path")
    };

    w.writeln("-- ───────────────────────────────────────────────────────────────");
    w.writeln(format!("-- {name} ({strength_note})"));
    w.writeln(format!("-- Extracted: {extracted}"));
    w.writeln(format!("-- Spec:      {spec_comment}"));
    w.writeln("-- ───────────────────────────────────────────────────────────────");
    w.newline();

    let struct_name = format!("{name}Corresponds");
    w.writeln(format!("structure {struct_name}"));
    w.writeln(format!("    (ec : {extracted})"));
    w.writeln(format!("    (sc : {spec}) : Prop where"));
    w.indent();
    for f in fields {
        let fname = escape_lean_keyword(&f.name);
        let eq_expr = direct_field_correspondence(&fname, &f.ty, registry);
        w.writeln(format!("{fname}_eq : {eq_expr}"));
    }
    w.dedent();
    w.newline();
}

/// Parse an existing BinaryBridge Lean file for `extracted_name → spec_path` mappings.
///
/// This is a standalone parser for use in direct mode (no charon_lib dependency).
/// It recognises the same two patterns as `bridge::parse_spec_map`:
///
/// ```text
/// def {name}ToSpec : Extracted.{ext} → {spec}
/// structure {name}Corresponds
///     (ec : Extracted.{ext})
///     (sc : {spec}) : Prop where
/// ```
pub fn parse_spec_map_direct(path: &Path) -> HashMap<String, String> {
    let content = match std::fs::read_to_string(path) {
        Ok(s) => s,
        Err(e) => {
            eprintln!(
                "styx: warning: cannot read spec-map file {}: {e}",
                path.display()
            );
            return HashMap::new();
        }
    };
    parse_spec_map_direct_str(&content)
}

fn parse_spec_map_direct_str(content: &str) -> HashMap<String, String> {
    let mut map = HashMap::new();
    let lines: Vec<&str> = content.lines().collect();

    let mut i = 0;
    while i < lines.len() {
        let line = lines[i].trim();

        // Pattern 1: `def {name}ToSpec : Extracted.{ext} → {spec}`
        if line.starts_with("def ") && line.contains("ToSpec")
            && let Some(colon) = line.find(':') {
                let sig = line[colon + 1..].trim();
                if let Some(ext_name) = extract_extracted_name_direct(sig)
                    && let Some(spec) = extract_spec_path_from_arrow(sig) {
                        map.insert(ext_name, spec);
                    }
            }

        // Pattern 2: `structure {name}Corresponds` (multi-line)
        if line.starts_with("structure ") && line.contains("Corresponds") {
            let window: String = lines[i..lines.len().min(i + 5)]
                .iter()
                .map(|l| l.trim())
                .collect::<Vec<_>>()
                .join(" ");
            if let Some(ext_name) = extract_extracted_name_direct(&window)
                && let Some(spec) = extract_spec_path_from_sc(&window) {
                    map.insert(ext_name, spec);
                }
        }

        i += 1;
    }
    map
}

/// Extract simple extracted type name from `"Extracted.Foo → ..."` or `"... (ec : Extracted.Foo) ..."`.
fn extract_extracted_name_direct(s: &str) -> Option<String> {
    let prefix = "Extracted.";
    let start = s.find(prefix)? + prefix.len();
    let rest = &s[start..];
    let end = rest
        .find(|c: char| c.is_whitespace() || c == ')')
        .unwrap_or(rest.len());
    let name = rest[..end].trim().to_string();
    if name.is_empty() { None } else { Some(name) }
}

/// Extract spec path from `"Extracted.X → SpecPath"` or `"Extracted.X → SpecPath "`.
fn extract_spec_path_from_arrow(sig: &str) -> Option<String> {
    let arrow_pos = sig
        .find('→')
        .map(|p| (p, '→'.len_utf8()))
        .or_else(|| sig.find("->").map(|p| (p, 2)))?;
    let after = sig[arrow_pos.0 + arrow_pos.1..].trim();
    let end = after
        .find(|c: char| c.is_whitespace())
        .unwrap_or(after.len());
    let spec = after[..end].trim().to_string();
    if spec.is_empty() { None } else { Some(spec) }
}

/// Extract spec path from `"... (sc : SpecPath) ..."` or `"... (sx : SpecPath) ..."`.
fn extract_spec_path_from_sc(s: &str) -> Option<String> {
    let marker = s
        .find("sc :")
        .map(|p| (p, "sc :".len()))
        .or_else(|| s.find("sx :").map(|p| (p, "sx :".len())))?;
    let after = s[marker.0 + marker.1..].trim();
    let end = after
        .find(|c: char| c == ')' || c.is_whitespace())
        .unwrap_or(after.len());
    let spec = after[..end].trim().to_string();
    if spec.is_empty() { None } else { Some(spec) }
}

/// Emit a `Bridge_Template.lean` from directly-parsed Rust types.
///
/// Works without LLBC/Charon — operates purely on `ParsedCrate` types.
/// Classification logic:
/// - `RustEnum` with all unit variants → BIJECTIVE
/// - `RustEnum` with data variants    → INJECTIVE
/// - `RustStruct`                     → RELATIONAL
///
/// If `spec_map` is provided, spec type paths are resolved from the map
/// instead of using the heuristic `Lion.X` derivation.
pub fn emit_bridge_template_direct(
    parsed: &ParsedCrate,
    namespace: &str,
    spec_map: Option<&HashMap<String, String>>,
) -> String {
    let rules = RewriteRules::standard();
    let mut w = IndentWriter::new();

    // Preamble
    w.writeln("-- ═══════════════════════════════════════════════════════════════");
    w.writeln("-- Bridge Template: auto-generated correspondence proof outlines.");
    w.writeln("-- Generated by Styx (direct mode). Fill in proof bodies.");
    w.writeln("-- ═══════════════════════════════════════════════════════════════");
    w.newline();
    w.writeln(format!("import {namespace}.Types"));
    w.writeln("-- TODO: import your spec types (e.g., import Lion.Core.Types)");
    w.newline();
    w.writeln("set_option linter.dupNamespace false");
    w.writeln("set_option linter.unusedVariables false");
    w.newline();
    w.writeln(format!("namespace {namespace}.Bridge"));
    w.newline();

    // Filter types: skip std/core/alloc and aliases.
    let types: Vec<&ParsedTypeDef> = parsed
        .types
        .iter()
        .filter(|t| {
            !t.name.starts_with("std.")
                && !t.name.starts_with("core.")
                && !t.name.starts_with("alloc.")
        })
        .collect();

    // Build registry: simple_name → DirectBridgeKind (for field correspondence lookups).
    let mut registry: DirectRegistry = HashMap::new();
    for def in &types {
        if let Some(kind) = classify_direct(def) {
            registry.insert(def.simple_name.clone(), kind);
        }
    }

    let mut count_bijective = 0u32;
    let mut count_injective = 0u32;
    let mut count_relational = 0u32;

    for def in &types {
        let bridge_kind = match classify_direct(def) {
            Some(k) => k,
            None => continue,
        };

        let name = &def.simple_name;
        let (spec, spec_known) = derive_direct_spec_name(name, spec_map);

        // Emit generic-params comment if type has parameters (bridge doesn't support generics yet).
        if !def.type_params.is_empty() {
            w.writeln(format!(
                "-- NOTE: {name} has generic params {:?} — bridge template is monomorphised. Adjust as needed.",
                def.type_params
            ));
        }

        match bridge_kind {
            DirectBridgeKind::Bijective => {
                if let ParsedTypeKind::Enum(variants) = &def.kind {
                    emit_direct_bijective(&mut w, name, namespace, &spec, spec_known, variants);
                    count_bijective += 1;
                }
            }
            DirectBridgeKind::Injective => {
                if let ParsedTypeKind::Enum(variants) = &def.kind {
                    emit_direct_injective(&mut w, name, namespace, &spec, spec_known, variants);
                    count_injective += 1;
                }
            }
            DirectBridgeKind::Relational => {
                if let ParsedTypeKind::Struct(fields) = &def.kind {
                    emit_direct_relational(
                        &mut w, name, namespace, &spec, spec_known, fields, &registry,
                    );
                    count_relational += 1;
                }
            }
        }
    }

    // Drop unused `rules` — it's needed only for type printing (not used in bridge output here).
    drop(rules);

    eprintln!(
        "styx bridge (direct): {} types scanned, {} emitted ({} bijective, {} injective, {} relational)",
        types.len(),
        count_bijective + count_injective + count_relational,
        count_bijective,
        count_injective,
        count_relational
    );

    // Summary
    w.writeln("-- ═══════════════════════════════════════════════════════════════");
    w.writeln(format!(
        "-- Summary: {} BIJECTIVE, {} INJECTIVE, {} RELATIONAL",
        count_bijective, count_injective, count_relational
    ));
    w.writeln(format!(
        "-- Total: {} type correspondences",
        count_bijective + count_injective + count_relational
    ));
    w.writeln("-- ═══════════════════════════════════════════════════════════════");
    w.newline();

    w.writeln(format!("end {namespace}.Bridge"));

    w.finish()
}
