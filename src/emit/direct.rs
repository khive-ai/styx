/// Format generic type parameters for a Lean declaration.
fn format_generic_params(type_params: &[String]) -> String {
    if type_params.is_empty() {
        String::new()
    } else {
        let parts: Vec<String> = type_params
            .iter()
            .map(|p| format!(" ({p} : Type)"))
            .collect();
        parts.join("")
    }
}

/// Format function parameters as Lean syntax.
///
/// If `self_type` is provided, occurrences of `Self` in types are resolved
/// to the concrete type name.
fn format_params(
    params: &[(String, RustType)],
    rules: &RewriteRules,
    self_type: Option<&str>,
) -> String {
    if params.is_empty() {
        return String::new();
    }

    let parts: Vec<String> = params
        .iter()
        .map(|(name, ty)| {
            let lean_ty = resolve_self(&rust_type_to_lean(ty, rules), self_type);
            let escaped = escape_lean_keyword(name);
            format!(" ({escaped} : {lean_ty})")
        })
        .collect();

    parts.join("")
}

/// Format the return type for a function declaration.
///
/// For `&mut self` methods, the return wraps the original return type
/// with the updated self: `Result (ReturnType x SelfType)`.
fn format_return_type(f: &ParsedFnSig, rules: &RewriteRules) -> String {
    let base_ret = resolve_self(
        &rust_type_to_lean(&f.ret_type, rules),
        f.self_type.as_deref(),
    );

    if f.is_mut_self {
        if let Some(ref st) = f.self_type {
            let lean_self = rust_type_to_lean(&RustType::Named(st.clone()), rules);
            // For &mut self methods, return includes updated self.
            // If return is Unit, just return the self type.
            // Otherwise return a product.
            match &f.ret_type {
                RustType::Unit => format!("Result {lean_self}"),
                _ => format!("Result ({base_ret} × {lean_self})"),
            }
        } else {
            format!("Result {base_ret}")
        }
    } else {
        format!("Result {base_ret}")
    }
}

/// Replace `Self` with the concrete type name in a Lean type string.
fn resolve_self(lean_ty: &str, self_type: Option<&str>) -> String {
    match self_type {
        Some(st) => lean_ty.replace("Self", st),
        None => lean_ty.to_string(),
    }
}

