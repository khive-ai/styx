pub(crate) fn emit_header(w: &mut IndentWriter) {
    w.writeln("-- THIS FILE WAS AUTOMATICALLY GENERATED BY STYX.");
    w.writeln("-- DO NOT EDIT.");
    w.newline();
    w.writeln("import Aeneas");
}

pub(crate) fn emit_options(w: &mut IndentWriter) {
    w.newline();
    w.writeln("set_option linter.dupNamespace false");
    w.writeln("set_option linter.hashCommand false");
    w.writeln("set_option linter.unusedVariables false");
    w.newline();
    w.writeln("open Aeneas.Std Result Error");
}
