/// Emit Lean4 files from a StyxPackage.
pub fn emit_styx_package(
    pkg: &StyxPackage,
    namespace: &str,
    output_dir: &PathBuf,
    aeneas_compat: bool,
    emit_checks: bool,
    skip_stdlib: bool,
) -> BTreeMap<PathBuf, String> {
    let mut files = BTreeMap::new();
    let ns_dir = output_dir.join(namespace);

    let types_content = emit_types_file(pkg, namespace, emit_checks);
    files.insert(ns_dir.join("Types.lean"), types_content);

    let emit_result = emit_funs_file(pkg, namespace, aeneas_compat, emit_checks, skip_stdlib);
    files.insert(ns_dir.join("Funs.lean"), emit_result.funs_lean);

    let ext_content = emit_funs_external(pkg, namespace, &emit_result.sorry_fns, skip_stdlib);
    files.insert(ns_dir.join("FunsExternal.lean"), ext_content.clone());
    files.insert(ns_dir.join("FunsExternal_Template.lean"), ext_content);

    files
}

